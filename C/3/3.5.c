/*
逻辑运算符：
逻辑与：&& (结果都为真输出才为真)
逻辑或：|| (随意一个结果为真那输出也是真)
逻辑非：! (如果结果为真，输出反之为假)
*/
#include <stdio.h>//打开一个工具库 stdio.h为库名
int main()//主函数：程序的入口  有且只有一个，而且必须要有一个 (头文件)
{
    //0假 1真

    //与
    int a = 10,b = 10;
    int c = (a>10) && (b==10);
    printf("%d\n",c);
    //或
    int d = 10,e = 10;
    int f =(d>e) || (d==e);
    printf("%d\n",f);
    //非
    int g = 10;
    int h = !(g>10);
    printf("%d",h);
    return 0;//代码结束
}